export const getGen2ItemName = (id: number): string => {
  if (id >= 125 && id <= 131) {
    return `HM0${id - 124}`;
  }
  if (id >= 132 && id <= 181) {
    return `TM${(id - 131).toString().padStart(2, '0')}`;
  }
  return GEN2_ITEMS[id] || (id === 0 ? 'None' : `Item ${id}`);
};

